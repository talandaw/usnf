            <div class="row">
                <div class="col-md-12">
                    “Atarashi” Naginata is the contemporary version of the practice of Naginata, begun in Japan after
                    the Second World War. It incorporates sports competition elements and techniques to the practice
                    that keep the art interesting and relevent in the modern age. Practice includes the use of both kata
                    (choreographed practice) and shiai (full-contact sparring with flexible weapons and armor). The All
                    Japan Naginata Federation sets technical specifications for regulations and ranking. The term
                    “Atarashii Naginata” refers to dojo recognized by the AJNF and the International Naginata
